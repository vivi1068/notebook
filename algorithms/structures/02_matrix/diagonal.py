

# 对角矩阵(square matrix)
# 当 i != j 时, M(i, j) = '0'
def diagonal_matrix():
    result = [['0'] * 9 for i in range(9)]

    for i in range(9):
        for j in range(9):
            if i == j:
                result[i][j] = "x"

    return result


def main():
    ss = diagonal_matrix()
    assert ss == [['x', '0', '0', '0', '0', '0', '0', '0', '0'],
                  ['0', 'x', '0', '0', '0', '0', '0', '0', '0'],
                  ['0', '0', 'x', '0', '0', '0', '0', '0', '0'],
                  ['0', '0', '0', 'x', '0', '0', '0', '0', '0'],
                  ['0', '0', '0', '0', 'x', '0', '0', '0', '0'],
                  ['0', '0', '0', '0', '0', 'x', '0', '0', '0'],
                  ['0', '0', '0', '0', '0', '0', 'x', '0', '0'],
                  ['0', '0', '0', '0', '0', '0', '0', 'x', '0'],
                  ['0', '0', '0', '0', '0', '0', '0', '0', 'x']]


if __name__ == '__main__':
    main()
